// Sentencias switch
let nota = 4;
 switch (nota)
 {
    case 5:
        console.log ("Buen trabajo, Sobre saiente");
        break
    case 4:
        console.log ("buen rabajo, pero puedes mejorar");
        break
    case 3:
        console.log ("haz obtenido un sufuciente");
        break
    case 2:
        console.log ("No haz aprobado por poco");
        break
    case 1:
        console.log ("No haz estudiado lo suficiente")
        break
    default:
        console.log ("Error")
     break;
 }